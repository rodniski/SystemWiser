// Função personalizada para achatar as cores
function flattenColors(colors, prefix = '', result = {}) {
  for (const [key, value] of Object.entries(colors)) {
    if (typeof value === 'string') {
      result[`${prefix}${key}`] = value;
    } else if (typeof value === 'object') {
      flattenColors(value, `${prefix}${key}-`, result);
    }
  }
  return result;
}

// Plugin personalizado para adicionar variáveis CSS para cores
function addVariablesForColors({ addBase, theme }) {
  const colors = theme("colors");
  const flattenedColors = flattenColors(colors);
  const newVars = Object.fromEntries(
    Object.entries(flattenedColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ":root": newVars,
  });
}

